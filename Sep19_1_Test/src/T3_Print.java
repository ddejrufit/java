public class T3_Print {
//	main -> ctrl + space + enter
	public static void main(String[] args) {
		System.out.println(); // syso + 자동완성
		System.out.println("println : 콘솔창에 글자를 출력");
		System.out.println("줄바꿈 효과");
		System.out.print("print : 글자출력, 줄바꿈X");
		System.out.println(); // 그냥 엔터 효과
		System.out.println("안녕 디지몬");
		System.out.println("10 + 2"); // 숫자가 글자로 인식
		System.out.println(10 + 2); // 숫자 계산할 때
		System.out.println(10 - 2);
		System.out.println(10 * 2);
		System.out.println(10 / 2);
	}
}







